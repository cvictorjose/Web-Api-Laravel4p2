				<p>
					<span style="color:#b22222;font-size: 14px;"><strong>5. Clausola Penale.</strong> </span></p>
				<p>
					5.1. Safe Bag e il Cliente concordano che, in caso di Evento Dannoso, la Società sarà tenuta a corrispondere al Cliente una somma di denaro quantificata secondo quanto di seguito previsto, rimanendo escluso il risarcimento del danno ulteriore (la “Penale da parte di Safe Bag” o anche solo la “Penale”).</p>
				<p>
					5.2. In caso di Smarrimento del Bagaglio ovvero di Danneggiamento del Bagaglio, la Penale da parte di Safe Bag sarà corrisposta solo dopo che il Cliente avrà percepito l’Indennizzo della Compagnia Aerea, e per una somma pari a tale indennizzo fino ai seguenti massimali: €4.000, in caso di Smarrimento del Bagaglio; €1.000, in caso di Danneggiamento del Bagaglio.</p>
				<p>
					A titolo esemplificativo, in caso di Danneggiamento del Bagaglio:</p>
				<ul style="margin-left: 40px;">
					<li>
						se l’Indennizzo della Compagnia Aerea è pari a €500, anche la Penale da parte di Safe Bag sarà pari a €500, per un totale di €1.000;</li>
					<li>
						se l’Indennizzo della Compagnia Aerea è pari a €1.000 o superiore, la Penale da parte di Safe Bag sarà pari a €1.000;</li>
					<li>
						se il vettore aereo non eroga alcun indennizzo, anche Safe Bag non erogherà alcun risarcimento.</li>
				</ul>
				<p>
					In ogni caso l’ammontare complessivo delle somme erogate al Cliente dalla Compagnia Aerea e da Safe Bag non potrà superare il valore del Bagaglio dichiarato dal Cliente.</p>
				<p>
					5.3. In caso di mancato ritrovamento del Bagaglio entro il termine di cui all’articolo 3.3., la Penale sarà pari a €50 al giorno per ogni giorno successivo allo spirare del termine di cui all’articolo 3.3. fino all’effettivo ritrovamento del Bagaglio. La Penale non potrà in ogni caso superare la somma di €500, anche qualora decorrano oltre 10 (dieci) giorni dallo spirare del termine di cui all’articolo 3.3.</p>
				<p>
					5.4. Il sorgere dell’obbligo al pagamento della Penale da parte della Società è subordinato ai seguenti requisiti:</p>
				<ul style="margin-left: 40px;list-style:none;">
					<li>
						a) il Bagaglio sia stato avvolto con il sistema Safe Bag e sia stato registrato e consegnato al momento del check-in (baggage tag) al personale aeroportuale autorizzato;</li>
					<li>
						b) il Cliente abbia attivato il servizio tramite Safe Bag Mobile App o Safe Bag Web Site prima della partenza;</li>
					<li>
						c) il Cliente abbia effettuato le dovute comunicazioni entro i termini previsti e abbia rispettato le Istruzioni in Caso di Eventi Dannosi descritte nel successivo articolo 6.</li>
				</ul>
				<p>
					5.5. L’erogazione della Penale da parte di Safe Bag è subordinata alla consegna dei seguenti documenti:</p>
				<ul dir="ltr" style="margin-left: 40px;list-style:none;">
					<li>
						a) Copia del modulo P.I.R. (Property Irregularity Report), Rapporto di irregolarità Bagaglio predisposto dall’ufficio Lost&amp;Found dell’aeroporto;</li>
					<li>
						b) Copia delle ricevute fiscali di acquisto del servizio;</li>
					<li>
						c) Copia del biglietto aereo, delle carte d’imbarco e dei baggage tag rilasciati dal vettore aereo;</li>
					<li>
						d) Copia della quietanza della compagnia aerea con l’indicazione dell’ammontare dell’indennizzo (ove necessario);</li>
					<li>
						e)Copia del bonifico o altra prova dell’avvenuto pagamento dell’Indennizzo della Compagnia Aerea (ove necessario);</li>
					<li>
						f) Copia della lettera di vettura del corriere che ha provveduto alla riconsegna del Bagaglio al Cliente o documento rilasciato dalla compagnia aerea al momento del ritiro del Bagaglio in aeroporto, che attesti la data di rintracciamento del Bagaglio (ove necessario).</li>
				</ul>
				<p>
					5.6. Safe Bag e il Cliente concordano che, qualora si verifichi un Danneggiamento del Bagaglio per mano dell’Operatore di Safe Bag, la Società sarà tenuta a corrispondere al Cliente una somma di denaro quantificata di comune accordo con il Cliente sulla base degli effettivi danni riportati dal Bagaglio e comunque non superiore alla somma di €150.</p>
				<p>
					5.7. Resta inteso tra le parti che: i) qualora il Bagaglio sia rintracciato dopo 21 (giorni) dalla Mancata Consegna e si sia verificato un Danneggiamento del Bagaglio, il Cliente avrà diritto di ottenere il pagamento della sola Penale concernente lo Smarrimento del Bagaglio; ii) la Penale percepita dal Cliente per il mancato ritrovamento del Bagaglio nel termine di cui all’art. 3.3. non potrà essere cumulata con la Penale per lo Smarrimento del Bagaglio.</p>
				<p>
					5.8. Resta inteso che il Cliente non avrà diritto al pagamento di alcuna Penale o comunque ad alcun risarcimento: i) nel caso in cui comunichi gli Eventi Dannosi oltre i termini previsti nelle Istruzioni in Caso di Eventi Dannosi descritte nel successivo articolo 6; ii) nel caso in cui comunichi a Safe Bag gli Eventi Dannosi tramite modalità diverse da quelle indicate nelle Istruzioni in Caso di Eventi Dannosi di seguito descritte; iii) qualora il Cliente non fornisca adeguata documentazione dell’avvenuto pagamento dell’Indennizzo della Compagnia Aerea e del suo ammontare, nel caso di Danneggiamento del Bagaglio e Smarrimento del Bagaglio; iv) qualora il Cliente non fornisca copia del documento che certifichi la data di avvenuta riconsegna del Bagaglio al Cliente da parte della compagnia aerea, nel caso in cui il Bagaglio non sia ritrovato nel termine di cui all’articolo 3.3.; v) per le spese sostenute per l’acquisto di beni e ogni altra spesa o disagio derivante dalla Mancata Riconsegna del Bagaglio e/o dal Danneggiamento del Bagaglio.5.9. È espressamente escluso il risarcimento del danno ulteriore rispetto alla Penale riconosciuta al Cliente in forza del presente articolo, che esaurisce pertanto ogni obbligo di Safe Bag verso il Cliente in conseguenza dell’Evento Dannoso.</p>
				<p>
					&nbsp;</p>
				<p>
					<span style="color:#b22222;font-size: 14px;"><strong>6. Istruzioni in Caso di Eventi Dannosi.</strong> </span></p>
				<p>
					<strong><u>- Verifica dello stato del Bagaglio</u></strong></p>
				<p>
					Al momento della consegna del Bagaglio da parte del vettore aereo presso l’aeroporto di destinazione, il Cliente è tenuto a togliere immediatamente il film protettivo e verificare eventuali danni prima di lasciare la zona di consegna dell’aeroporto di destinazione.</p>
				<p>
					<strong><u>- Compilazione del modulo PIR presso l’ufficio Lost&amp;Found dell’aeroporto di destinazione</u></strong></p>
				<p>
					In caso di Danneggiamento del Bagaglio e/o Mancata Riconsegna del Bagaglio all’aeroporto di destinazione, il Cliente deve immediatamente recarsi presso l’ufficio Lost&amp;Found e compilare il modulo P.I.R. (Property Irregularity Report) prima di lasciare l’aeroporto medesimo.</p>
				<p>
					<strong><u>- Denuncia al Vettore Aereo</u></strong></p>
				<p>
					Dopo aver compilato il modulo P.I.R., il Cliente dovrà denunciare il Danneggiamento del Bagaglio e/o la Mancata Consegna del Bagaglio al vettore aereo, per attivare le procedure necessarie all’erogazione dell’Indennizzo della Compagnia Aerea.</p>
				<p>
					<strong><u>- Richiesta di Risarcimento a Safe Bag S.p.A. entro i termini previsti</u></strong></p>
				<p>
					Entro e non oltre 21 (giorni) (i) dalla data di consegna in caso di Danneggiamento del Bagaglio; dal Danneggiamento del Bagaglio per mano dell’Operatore Safe Bag; e (ii) dallo spirare del termine di cui all’art. 3.3., il Cliente è tenuto a chiedere il risarcimento tramite la procedura guidata direttamente presso la Safe Bag Web Site o, in alternativa, mediante l’invio di una raccomandata a/r, indirizzata a Safe Bag S.p.A., Ufficio Customer Care, Via Fonte di Fauno 6/A, 00153 Rome, Italy (la “Richiesta di Risarcimento”). Qualora si verifichi un Danneggiamento del Bagaglio per mano dell’Operatore di Safe Bag, il Cliente dovrà inoltre prontamente comunicare all’operatore l’avvenuto danno e compilare l’apposito modulo (il “Modulo Risarcimento Danno Operatore”).</p>
				<p>
					<strong><u>- Raccolta documenti e compilazione e invio del Modulo Risarcimento e Modulo di Quietanza</u></strong></p>
				<p>
					In seguito alla Richiesta di Risarcimento nei tempi previsti, il Cliente riceverà via posta elettronica un modulo precompilato con i dati raccolti e con le indicazioni da seguire per l’ottenimento del pagamento del risarcimento dei danni dovuto da Safe Bag secondo quanto previsto dal precedente articolo 5 (il “Modulo Risarcimento”).</p>
				<p>
					Il Cliente è tenuto a inserire, in una sezione dedicata del Safe Bag Web Site, una copia elettronica di ciascuno dei seguenti documenti:</p>
				<ul style="margin-left: 40px;">
					<li>
						Modulo Risarcimento firmato e debitamente compilato;</li>
					<li>
						foto del danno (ove necessario);</li>
					<li>
						ricevute fiscali Safe Bag;</li>
					<li>
						documento di identità in corso di validità del Cliente;</li>
					<li>
						Modulo Risarcimento Danno Operatore (ove necessario);</li>
					<li>
						tutte le carte d'imbarco dall'aeroporto di partenza alla destinazione finale;</li>
					<li>
						rapporto di irregolarità rilasciato dal Lost&amp;Found (Modulo P.I.R.);</li>
					<li>
						dichiarazione liberatoria firmata dal vettore aereo con l’ammontare dell’indennizzo erogato (ove necessario);</li>
					<li>
						copia del bonifico o assegno ricevuto dal vettore con l’ammontare dell’indennizzo erogato (ove necessario);</li>
					<li>
						copia della lettera di vettura del corriere che ha provveduto alla riconsegna del Bagaglio al Cliente o documento rilasciato dalla compagnia aerea al momento del ritiro del Bagaglio in aeroporto, che attesti la data di rintracciamento del Bagaglio (ove necessario).</li>
				</ul>
				<p>
					Ricevuti tutti i suddetti documenti, il Customer Care di Safe Bag potrà elaborare la richiesta di risarcimento dei danni per le dovute verifiche. In caso non siano ravvisate anomalie, il Cliente riceverà via email un “Modulo di Quietanza”.</p>
				<p>
					Il Cliente è tenuto a inserire in un’apposita sezione del Safe Bag Web Site una copia elettronica firmata e debitamente compilata del “Modulo di Quietanza” e a provvedere all’invio dell’originale firmato a Safe Bag S.p.A., Ufficio Customer Care, Via Fonte di Fauno 6/A, 00153 Rome, Italy o Via fax al numero: +39 0669307477. Solo al momento della ricezione del Modulo di Quietanza originale debitamente compilato e firmato via raccomandata a/r o fax, Safe Bag potrà procedere al pagamento.</p>
				<p>
					Trascorsi 6 mesi dalla richiesta del risarcimento a Safe Bag, in caso di mancata consegna dei documenti richiesti, o di qualsiasi altro ritardo nella gestione del processo di gestione dell’Evento Dannoso non imputabile a Safe Bag S.p.A, la richiesta del Cliente verrà archiviata senza alcun pagamento.</p>
