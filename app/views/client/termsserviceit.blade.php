<h2 class="titolo">Condizioni Generali di Prestazione dei Servizi</h2>
<br>
			<!--contact info-->
            <p>
					Basic Protection e Smart Tracking sono i servizi offerti da Safe Bag S.p.A. (di seguito “Safe Bag” o la “Società”) la quale può:</p>
				<ul style="margin-left: 40px;">
					<li>
						Avvolgere il tuo Bagaglio in un film protettivo, garantendo che lo stesso non venga danneggiato durante il volo e durante il processo di avvolgimento;</li>
					<li>
						Rendere riconoscibile e univocamente identificabile il tuo Bagaglio;</li>
					<li>
						Supportare nella ricerca del Bagaglio in caso di smarrimento da parte del vettore aereo, nei limiti e secondo le modalità di seguito precisati.</li>
				</ul>
				<p>
					<span style="font-size:14px;color: rgb(165, 42, 42);"><strong>1. Definizioni.</strong></span><br>
					Nelle presenti Condizioni Generali di Prestazione dei Servizi, i termini e le espressioni riportate nel prosieguo avranno il seguente significato:</p>
				<ul style="margin-left: 40px;">
					<li>
						<strong>Bagaglio</strong>: qualsiasi contenitore trattato con i servizi offerti da Safe Bag con esclusione del relativo contenuto.</li>
					<li>
						<strong>Cliente</strong>: il passeggero di qualsiasi vettore aereo che acquisti i servizi offerti da Safe Bag.</li>
					<li>
						<strong>Danneggiamento del Bagaglio</strong>: rottura delle parti esterne del Bagaglio (incluse serrature, cerniere, ruote e manici), che renda lo stesso inutilizzabile. Sono perciò esclusi: graffi, piccoli strappi, qualsiasi danno alle parti interne del Bagaglio, nonché qualsiasi danno relativo al suo contenuto. Restano altresì esclusi i danni arrecati agli oggetti avvolti con il film protettivo che siano privi di contenitore (a titolo esemplificativo: passeggini e attrezzature sportive).</li>
					<li>
						<strong>Danneggiamento del Bagaglio per mano dell’Operatore di Safe Bag</strong>: rottura delle parti esterne del Bagaglio (incluse serrature, cerniere, ruote e manici) da parte dell’operatore Safe Bag, durante il processo di avvolgimento presso l’aeroporto di partenza.</li>
					<li>
						<strong>Eventi Dannosi</strong>: mancato ritrovamento del Bagaglio entro il termine di cui all’art. 3.3., Danneggiamento del Bagaglio, Danneggiamento del Bagaglio per mano dell’Operatore di Safe Bag, Smarrimento del Bagaglio.</li>
					<li>
						<strong>Indennizzo della Compagnia Aerea</strong>: l’ammontare riconosciuto dalla Compagnia Aerea al Cliente nel caso in cui si verifichi un Evento Dannoso ad essa imputabile.</li>
					<li>
						<strong>Modulo Risarcimento</strong>: ha il significato di cui al successivo articolo 6.</li>
					<li>
						<strong>Mancata Consegna del Bagaglio</strong>: la mancata consegna del Bagaglio all’arrivo nell’aeroporto di destinazione.</li>
					<li>
						<strong>Penale o Penale da parte di Safe Bag</strong>: hanno il significato di cui al successivo articolo 5.1.</li>
					<li>
						<strong>Richiesta di Risarcimento</strong>: ha il significato di cui all’articolo 6.</li>
					<li>
						<strong>Safe Bag Code</strong>: codice univoco di 14 caratteri associato a ciascun Bagaglio, trattato da Safe Bag.</li>
					<li>
						<strong>Safe Bag Mobile Ap</strong>p: applicazione per dispositivi mobili con sistema operativo iOS ed Android disponibile sulle piattaforme Apple Store e Google Play.</li>
					<li>
						<strong>Safe Bag Web Site</strong>: sito web di Safe Bag accessibile all’indirizzo: www.safe-bag.com.</li>
					<li>
						<strong>Smarrimento del Bagaglio</strong>: il mancato ritrovamento del Bagaglio entro 21 (ventuno) giorni dalla Mancata Consegna del Bagaglio.</li>
				</ul>
				<p>
					&nbsp;</p>
				<p>
					<span style="color:#b22222;font-size: 14px;"><strong>2. Il Servizio Basic Protection.</strong></span></p>
				<p>
					2.1. Safe Bag si impegna ad avvolgere il Bagaglio con uno speciale film protettivo ultra resistente che consente un’efficace protezione e deterrenza contro rotture, danneggiamenti, graffi, pioggia, sporcizia, aperture accidentali, furto parziale e totale.</p>
				<p>
					Il film protettivo utilizzato per l’avvolgimento è creato per assicurare la massima protezione del Bagaglio e nel contempo garantire il rispetto dell’ambiente (100% Riciclabile). Il film è fornito in colore rosso acceso per rendere il Bagaglio facilmente identificabile in aeroporto ed è semplice da rimuovere dopo l’uso grazie al suo ridotto spessore (~17 micron).</p>
				<p>
					2.2.Safe Bag garantisce l’idoneità del film protettivo a impedire il Danneggiamento del Bagaglio fino a 21 (ventuno) giorni dal suo avvolgimento e garantisce altresì che durante il procedimento di avvolgimento non si verificherà alcun Danneggiamento del Bagaglio per mano dell’Operatore di Safe Bag. Conseguentemente, è a carico di Safe Bag la responsabilità, nei termini indicati nel successivo articolo 5, verso i Clienti per qualsiasi Danneggiamento del Bagaglio verificatosi entro 21 (ventuno) giorni dal suo avvolgimento ovvero per qualsiasi Danneggiamento del Bagaglio per mano dell’Operatore di Safe Bag, che sia stato comunicato alla Società secondo le modalità previste nelle Istruzioni in Caso di Eventi Dannosi.</p>
				<p>
					&nbsp;</p>
				<p>
					<span style="color:#b22222;font-size: 14px;"><strong>3. Il Servizio Smart Tracking.</strong></span></p>
				<p>
					3.1. Il ritrovamento del Bagaglio è garantito dall'applicazione di uno sticker sul medesimo in cui è riportato il Safe Bag Code e dal servizio SITA Worldtracer Unique Identification Service. Il Cliente è tenuto ad attivare il servizio prima della partenza del volo, attraverso la registrazione del Safe Bag Code tramite Safe Bag Mobile App o Safe Bag Web Site, indicando il proprio nominativo e i propri recapiti. La mancata registrazione da parte del Cliente prima della partenza comporta la non attivazione del servizio smartTracking.</p>
				<p>
					3.2. Safe Bag usufruisce del SITA Worldtracer Unique Identification Service (UIS). Tale servizio permette agli operatori dei Lost&amp;Found, affiliati SITA presso qualsiasi aeroporto, di inserire il Safe Bag Code, in un apposito campo del database dedicato alla descrizione dei bagagli ritrovati (OHD – On Hand Database). L’inserimento del Safe Bag Code nell’On Hand Database (OHD) genera un messaggio istantaneo verso la Società, la quale potrà incrociare i dati e contattare il Cliente per offrire il supporto necessario al recupero del Bagaglio così identificato.</p>
				<p>
					3.3. Safe Bag si impegna a far sì e garantisce che provvederà a rintracciare il Bagaglio entro 2 (due) giorni dalla data in cui, in ragione della Mancata Consegna del Bagaglio, il Cliente ha compilato il Modulo P.I.R. (Property Irregularity Report) presso il servizio Lost&amp;Found dell’aeroporto di destinazione. Il Servizio di smartTracking terminerà in ogni caso decorsi 21 (ventuno) giorni dalla Mancata Consegna del Bagaglio.</p>
				<p>
					3.4. Resta inteso che il Customer Care di Safe Bag offre un supporto al Cliente nella ricerca del Bagaglio non consegnato dal vettore aereo per agevolarne il recupero, ma non è responsabile dei processi di localizzazione dei vettori aerei o delle società di handling aeroportuale, né ha rapporto alcuno con le procedure interne di ricerca dei medesimi. Il Customer Care è disponibile dal lunedì al venerdì dalle 9.30 alle 13.00 e dalle 14.00 alle 18.00 GMT (+1) al: +39 06 91511041 o via email: customer.care@safe-bag.com.</p>
				<p>
					&nbsp;</p>
