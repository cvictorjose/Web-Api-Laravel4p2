<h2><span style="color:#b22222;">Informativa e richiesta consenso per trattamento di dati personali</span></h2>
<p>
	Documento informativo ai sensi e per gli effetti di cui all’articolo 13, D.Lgs. 30 giugno 2003 n. 196 In osservanza al D.Lgs. 30 giugno 2003 n. 196 (Testo unico Privacy) e successive variazioni, siamo a fornirLe le dovute informazioni in ordine al trattamento dei Suoi dati personali, in nostro possesso.</p>
<p>
	<strong><span style="font-size:14px;"><span style="color: rgb(178, 34, 34);">Finalità del trattamento per le quali si concede consenso laddove richiesto (art. 23 D.Lgs. 196/03)</span></span></strong></p>
<p>
	I dati personali volontariamente forniti saranno oggetto di trattamento per le seguenti finalità:</p>
<ul style="margin-left: 40px;">
	<li>
		attività amministrative strettamente connesse e strumentali alla gestione dei rapporti con la clientela (es.: acquisizione di informazioni preliminari alla conclusione di un contratto, esecuzione di operazioni sulla base degli obblighi derivanti da contratti conclusi con la clientela, ecc.);</li>
	<li>
		attività contabile (emissione di fatture, predisposizione di pagamenti) ed eventuale trasferimenti dati all’estero anche in paesi non U.E. nei limiti di legge;</li>
	<li>
		fornitura di beni e servizi e per la tutela delle posizioni creditorie da essi derivanti;</li>
	<li>
		attività informativa, per la formalizzazione di richieste di informazioni su prodotti, servizi, per la predisposizione di preventivi o offerte;</li>
	<li>
		eventi ed iniziative promozionali della società, iscrizione a mailing list, attività statistiche, commerciali, pubblicitarie.</li>
</ul>
<p>
	<strong><span style="font-size:14px;"><span style="color: rgb(178, 34, 34);">Modalità del trattamento - Conservazione</span></span></strong><br>
	Il trattamento sarà svolto in forma automatizzata e/o manuale, con modalità e strumenti volti a garantire la massima sicurezza e riservatezza, ad opera di soggetti di ciò appositamente incaricati in ottemperanza a quanto previsto dagli artt. 31 e segg. D.Lgs 196/03. I dati saranno conservati per un periodo non superiore agli scopi per i quali i dati sono stati raccolti e successivamente trattati.<br>
	<br>
	<span style="font-size:14px;"><span style="color: rgb(178, 34, 34);"><strong>Ambito di comunicazione e diffusione</strong></span></span><br>
	I Suoi dati, oggetto del trattamento, non saranno diffusi, potranno essere invece comunicati a società collegate a Safe Bag S.p.A., all’estero all’interno dell’Unione Europea, in conformità e nei limiti di cui all’art. 42 del D.Lgs. n. 196/2003. I dati personali potranno essere trasferiti all’estero in paesi extra UE nell’ambito e nei limiti previsti dall’art. 44 lett. b) del D.Lgs. n. 196/2003. Al riguardo si specifica che il titolare del trattamento dati opera esclusivamente con soggetti che abbiano aderito ai “Principi di approdo sicuro in materia di riservatezza” (accordo di Safe Harbor) al fine di ottemperare ai contratti o finalità connesse.</p>
<p>
	I dati potranno essere comunicati a terzi appartenenti alle seguenti categorie:</p>
<ul style="margin-left: 40px;">
	<li>
		soggetti che forniscono servizi per la gestione del sistema informativo usato da Safe Bag S.p.A. e delle reti di telecomunicazioni (ivi compresa la posta elettronica);</li>
	<li>
		società di servizi per l'acquisizione, la registrazione ed il trattamento di dati provenienti da documenti, o supporti forniti ed originati dagli stessi clienti ed aventi ad oggetto lavorazioni massive relative a pagamenti, effetti, assegni ed altri titoli;</li>
	<li>
		soggetti che svolgono attività di assistenza alla clientela (es. call center, help desk, ecc.);</li>
	<li>
		studi o società nell'ambito di rapporti di assistenza e consulenza;</li>
	<li>
		soggetti che svolgono adempimenti di controllo, revisione e certificazione delle attività poste in essere da Safe Bag S.p.A. anche nell'interesse della clientela.<br>
		&nbsp;</li>
</ul>
<p>
