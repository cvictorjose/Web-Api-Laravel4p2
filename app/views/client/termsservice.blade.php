				<p>
					3.1. The finding of the Baggage is ensured through the attachment of a sticker on it containing the Safe Bag Code, and from the SITA service World Tracer Unique Identification Service. The Customer is required to activate the service before flight departure through the registration of the Safe Bag Code via the Safe Bag Safe Mobile App or Safe Bag Web Site. The registration entails providing name and contact information. Failure of registration by the Customer prior to departure results in the non-activation of the smartTracking service.</p>
				<p>
					3.2. Safe Bag benefits from the SITA World Tracer Unique Identification Service (UIS). This service allows the Lost &amp; Found operators, SITA affiliates at any airport, to enter the Safe Bag Code in an field in the dedicated database for registering found luggage (OHD – On Hand Database). The inclusion of Safe Bag in the On Hand Database (OHD) generates an instant message to the Company, who is then able to cross-check the data and contact the Customer to provide the necessary support for the recovery of the Baggage identified.</p>
				<p>
					3.3. Safe Bag is committed to ensure that it will trace the Baggage within 2 (two) days from the date on which, because a Failure of Luggage Delivery, the Customer has filled out the Property Irregularity Report (hereinafter P.I.R) at the Lost &amp; Found of the destination airport. However, the smartTracking service will terminate in any event after the expiration of twenty one (21) days from the Failure of Luggage Delivery.</p>
