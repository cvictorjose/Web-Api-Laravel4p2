					The protective film used to wrap is designed to ensure maximum protection of your Baggage and at the same time is eco-friendly (100% recyclable). The film comes in bright red to make it easy to identify the Baggage at the airport and is easy to remove after use due to its reduced thickness (~ 17 microns).</p>
				<p>
					2.2. Safe Bag guarantees the suitability of the protective film to prevent luggage damage up to 21 (twenty-one) days from its wrapping, and also ensures that during the wrapping process there will be no damage by the Safe Bag Specialist. Consequently, it is Safe Bag’s responsibility, as set out in Article 5, towards the Customer for any Luggage Damage that occurs within 21 (twenty-one) days from its wrapping or for any Luggage Damage by the Safe Bag Specialist, which has been notified to the Company according to the Instructions in Damaging Events.</p>
