<script type="text/javascript">
function submitcontactas(){	
	$("#loadingcontact").show();
	$(".required").html("");
	data	=	$( "#contactus-form" ).serializeArray();
	url	=	"{{ URL::to('client/contactus') }}";
	$.ajax({
		type: 'post',
		url: url,
		dataType: 'json',
		data: data,
		success: function(data) {
			//$.unblockUI;
			if(data.redirect){
				window.location.href ="{{ URL::to('/') }}	";
			}
			if(data.fail) {
			  $.each(data.errors, function( index, value ) {
				/*  $( "#errordivforgot" ).append( '<div class="alert alert-danger">'+value+'</div>' );*/
				var errorDiv = '#contactus-form #'+index+'-error';
				$(errorDiv).addClass('required');
				$(errorDiv).empty().append(value);
			  });
			  		  		           
			} 
			if(data.success){
				alert("Email Sent Sussessfully");				
				location.reload();			
			}
			
			$("#loadingcontact").hide();
			//alert(data.message);
			
		}
	});
	return false;	
}
</script>